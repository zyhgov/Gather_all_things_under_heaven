// 初始化对比职位下拉框
function initCompareSelectors() {
  const levelSelectors = [document.getElementById('compare-level1'), document.getElementById('compare-level2')];
  const positionSelectors = [document.getElementById('compare-position1'), document.getElementById('compare-position2')];

  // 初始化职位选择器
  function initPositionSelector(levelEl, positionEl) {
    const level = levelEl.value;
    positionEl.innerHTML = '<option value="">请选择职位</option>';
    if (!level || !positionData[level]) return;

    positionData[level].forEach(pos => {
      const option = document.createElement('option');
      option.value = pos.name;
      option.textContent = pos.name;
      positionEl.appendChild(option);
    });
  }

  // 绑定层级变化事件
  levelSelectors.forEach((levelEl, index) => {
    levelEl.addEventListener('change', () => {
      const positionEl = positionSelectors[index];
      initPositionSelector(levelEl, positionEl);
    });
  });

  // 初始填充
  levelSelectors.forEach((levelEl, index) => {
    levelEl.dispatchEvent(new Event('change'));
  });
}

// 页面加载时初始化
window.addEventListener('DOMContentLoaded', () => {
  initCompareSelectors();
});

