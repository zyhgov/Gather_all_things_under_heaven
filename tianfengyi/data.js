function comparePositions() {
  const level1 = document.getElementById('compare-level1').value;
  const name1 = document.getElementById('compare-position1').value;
  const level2 = document.getElementById('compare-level2').value;
  const name2 = document.getElementById('compare-position2').value;

  if (!level1 || !name1 || !level2 || !name2 || name1 === name2) {
    alert('请选择两个不同的职位进行对比');
    return;
  }

  const pos1 = positionData[level1].find(p => p.name === name1);
  const pos2 = positionData[level2].find(p => p.name === name2);
  if (!pos1 || !pos2) return;

  // 设置职位名称
  document.getElementById('compare-name1').textContent = pos1.name;
  document.getElementById('compare-name2').textContent = pos2.name;

  // 生成对比数据
  const tbody = document.getElementById('compare-table-body');
  tbody.innerHTML = '';

  // 计算Q值
  const Q1 = (pos1.S + pos1.C) / pos1.Z;
  const Q2 = (pos2.S + pos2.C) / pos2.Z;

  // 生成对比行
  const createRow = (label, val1, val2) => {
    const tr = document.createElement('tr');
    tr.innerHTML = `
      <td class="py-3 px-6 font-medium">${label}</td>
      <td class="py-3 px-6 text-center">${val1}</td>
      <td class="py-3 px-6 text-center">${val2}</td>
      <td class="py-3 px-6 text-center">
        <span class="inline-block px-3 py-1 rounded-full text-sm font-semibold
          ${Math.abs(val1 - val2) > 50 ? 'bg-red-100 text-red-800' : 
            Math.abs(val1 - val2) > 20 ? 'bg-yellow-100 text-yellow-800' : 
            'bg-green-100 text-green-800'}">
          ${val1 > val2 ? '↑' : '↓'} ${Math.abs(val1 - val2).toFixed(2)}
        </span>
      </td>
    `;
    tbody.appendChild(tr);
  };

  // 插入对比数据
  createRow('权力值(S)', pos1.S, pos2.S);
  createRow('财政值(C)', pos1.C, pos2.C);
  createRow('职级(Z)', pos1.Z, pos2.Z);
  createRow('含权量(Q)', Q1.toFixed(2), Q2.toFixed(2));

  // 显示结果
  document.getElementById('compare-result').classList.remove('hidden');

  // ✅ 手动触发 MathJax 渲染
  MathJax.typesetPromise([document.getElementById('compare-result')]);

  // ✅ 添加综合分析
  const analysisDiv = document.getElementById('compare-analysis');
  if (Q1 > Q2) {
    analysisDiv.textContent = `【综合分析】${pos1.name} 的含权量（Q=${Q1.toFixed(2)}）高于 ${pos2.name}（Q=${Q2.toFixed(2)}），主要优势来自权力值和财政支配力。`;
  } else if (Q1 < Q2) {
    analysisDiv.textContent = `【综合分析】${pos2.name} 的含权量（Q=${Q2.toFixed(2)}）高于 ${pos1.name}（Q=${Q1.toFixed(2)}），主要优势来自职级和财政资源。`;
  } else {
    analysisDiv.textContent = `【综合分析】${pos1.name} 和 ${pos2.name} 的含权量相等（Q=${Q1.toFixed(2)}），说明两者在权力、财政和职级上达到平衡。`;
  }
  analysisDiv.classList.remove('hidden');
}